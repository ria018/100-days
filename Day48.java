package day48;
public class Day48 {
    public static void main(String[] args) {
       int tinggi = 4; 
        for (int i = 1; i <= tinggi; i++) {
            for (int j = 1; j <= tinggi; j++) {
                System.out.print("* ");
            }
            System.out.println();
        }
    }
    
}
